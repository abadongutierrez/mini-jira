    <Typography variant="body2" color="text.secondary" align="center" {...props}>
      {'Copyright Â© '}
      <Link color="inherit" href="https://x.com/@abaddon_gtz">
        Rafael Gutierrez (@abaddon_gtz)
      </Link>{' '}
      {new Date().getFullYear()}
      {'.'}
    </Typography>
